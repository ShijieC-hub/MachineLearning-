public class Key_Value {
    private int Key;
    private int Value;
    public Key_Value(int Key,int Value){
        this.Key = Key;
        this.Value = Value;
    }
    public int getKey(){
        return Key;
    }
    public void setKey(int Key){
        this.Key = Key;
    }
    public int getValue(){
        return Value;
    }
    public void setValue(int Value){
        this.Value = Value;
    }
}
